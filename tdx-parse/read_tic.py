import struct


def gettime(s):
    if s <= 120:
        re = s + 570
    else:
        re = s + 660
    return str(re / 60 % 24).zfill(2) + ':' + str(re % 60).zfill(2)


def getvolume(vol, fh):
    re = 0
    if vol <= 252:
        re = vol
    elif vol == 253:
        bin_tmp = fh.read(1)
        line_tmp = struct.unpack('<B', bin_tmp)
        re = vol + line_tmp[0]
    elif vol == 254:
        bin_tmp = fh.read(2)
        line_tmp = struct.unpack('<H', bin_tmp)
        re = vol + line_tmp[0]
    elif vol == 255:
        bin_tmp = fh.read(1)
        line_tmp = struct.unpack('<B', bin_tmp)
        re = 0xFFFF * line_tmp[0]
        bin_tmp = fh.read(2)
        line_tmp = struct.unpack('<H', bin_tmp)
        re += line_tmp[0] + 0xFF
    return re


def shl(val):
    val <<= 1
    if len(str(bin(val))) > 34:
        str1 = 'tmp = 0b' + str(bin(val))[len(str(bin(val)))-34+2:]
        exec(str1)
        return val
    return val


def get_tick_data_bin(datafile):
    items = []
    with open(datafile, 'rb') as fh:
        bin_data = fh.read(20)
        #line = struct.unpack('<IIHIHI', bin_data) # .idx: 开始日期 结束日期 天数 日期节点 日期对应的数据大小 unknown
        line = struct.unpack('<IHHHHII', bin_data)  # .ntc 日期 当天总记录数 volumeOffset volumeSize type 价格 成交量
        # 注：time、type待确认准确性
        items.append({
            'date': line[0],
            'time': (line[4] & 255), #type进行LOBYTE()操作获得
            'price': line[5],
            'volume': line[6],
            'type': line[4] >> 14,
            'count': line[1], # 当天总记录数
            'voloffset': line[2],
            'volsize': line[3]
        })

        # 时间和价格
        #bin_data = fh.read(line[2])
        bin_size = 32
        bin_data = fh.read(4)
        bin_row = struct.unpack('<I', bin_data)[0]
        for i in range(line[1] - 1):
            per = {}
            per['type'] = int(bin_row >> 31)

            bin_row = shl(bin_row)
            bin_size -= 1
            if bin_size == 0:
                bin_data = fh.read(4)
                bin_row = struct.unpack('<I', bin_data)[0]
                bin_size = 32

            # 解析时间
            checksum = 3
            go_out = False
            while not go_out:
                # LABEL1 begin
                checksum = (2 * checksum) | (bin_row >> 31)
                bin_row = shl(bin_row)
                bin_size -= 1
                if bin_size == 0:
                    bin_data = fh.read(4)
                    bin_row = struct.unpack('<I', bin_data)[0]
                    bin_size = 32
                idx = 0
                while True:
                    if HashTableDateTime[idx].values()[0] != checksum:
                        if HashTableDateTime[idx].values()[0] < checksum:
                            idx += 1
                            if idx < len(HashTableDateTime):
                                continue
                            else:
                                #print ' - goto LABEL1-1 -'
                                break
                        else:
                            #print ' - goto LABEL1-2 -'
                            break
                    else:
                        go_out = True
                        break
            per['time'] = int(items[-1]['time']) + HashTableDateTime[idx].keys()[0]

            # 解析价格
            checksum = 3
            go_out = False
            while not go_out:
                # LABEL2 begin
                checksum = (2 * checksum) | (bin_row >> 31)
                bin_row = shl(bin_row)
                bin_size -= 1
                if bin_size == 0:
                    bin_data = fh.read(4)
                    bin_row = struct.unpack('<I', bin_data)[0]
                    bin_size = 32
                idx = 0
                while True:
                    if HashTablePrice[idx].values()[0] != checksum:
                        if checksum > 0x3FFFFFF or HashTablePrice[idx].values()[0] <= checksum:
                            idx += 1
                            if idx < len(HashTablePrice):
                                continue
                            else:
                                #print ' - goto LABEL2 -'
                                break
                        else:
                            #print ' - goto LABEL2 -'
                            break
                    else:
                        go_out = True
                        break
            if idx != 4000 or line[0] < 20011112:
                per['price'] = int(items[-1]['price']) + HashTablePrice[idx].keys()[0]
            else:
                checksum = 0
                for idx in range(1, 33):
                    checksum = (2 * checksum) | (bin_row >> 31)
                    bin_row = shl(bin_row)
                    bin_size -= 1
                    if bin_size == 0:
                        bin_data = fh.read(4)
                        bin_row = struct.unpack('<I', bin_data)[0]
                        bin_size = 32
                per['price'] = int(items[-1]['price']) + checksum

            items.append(per)

        # 成交量
        for k in range(1, line[1]):
            bin_data = fh.read(1)
            line = struct.unpack('<B', bin_data)
            items[k]['volume'] = getvolume(line[0], fh)

    # output
    print("time\tprice\tvolume\ttype")
    for per in items:
        print("%s\t%.2f\t%d\t%d" % (gettime(per['time']), per['price'] / 100.0, per['volume'], per['type']))


